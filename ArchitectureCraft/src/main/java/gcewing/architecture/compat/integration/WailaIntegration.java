package gcewing.architecture.compat.integration;

import static gcewing.architecture.compat.BlockCompatUtils.getMetaFromBlockState;

import java.util.List;

import net.minecraft.entity.player.EntityPlayerMP;
import net.minecraft.item.ItemStack;
import net.minecraft.nbt.NBTTagCompound;
import net.minecraft.tileentity.TileEntity;
import net.minecraft.world.World;

import gcewing.architecture.common.block.BlockShape;
import gcewing.architecture.common.tile.TileShape;
import gcewing.architecture.compat.IBlockState;
import gcewing.architecture.util.Utils;
import mcp.mobius.waila.api.IWailaConfigHandler;
import mcp.mobius.waila.api.IWailaDataAccessor;
import mcp.mobius.waila.api.IWailaDataProvider;
import mcp.mobius.waila.api.IWailaRegistrar;

@SuppressWarnings("unused")
public class WailaIntegration implements IWailaDataProvider {

    @Override
    public ItemStack getWailaStack(IWailaDataAccessor accessor, IWailaConfigHandler config) {
        return null;
    }

    @Override
    public List<String> getWailaHead(ItemStack itemStack, List<String> currenttip, IWailaDataAccessor accessor,
            IWailaConfigHandler config) {
        return currenttip;
    }

    @Override
    public List<String> getWailaBody(ItemStack itemStack, List<String> currenttip, IWailaDataAccessor accessor,
            IWailaConfigHandler config) {
        if (accessor.getTileEntity() instanceof TileShape shape) {
            IBlockState baseBlock = shape.baseBlockState;
            currenttip.add(Utils.displayNameOfBlock(baseBlock.getBlock(), getMetaFromBlockState(baseBlock)));
        }

        return currenttip;
    }

    @Override
    public List<String> getWailaTail(ItemStack itemStack, List<String> currenttip, IWailaDataAccessor accessor,
            IWailaConfigHandler config) {
        return currenttip;
    }

    @Override
    public NBTTagCompound getNBTData(EntityPlayerMP player, TileEntity te, NBTTagCompound tag, World world, int x,
            int y, int z) {
        return tag;
    }

    public static void register(IWailaRegistrar registrar) {
        registrar.registerBodyProvider(new WailaIntegration(), BlockShape.class);
    }
}
